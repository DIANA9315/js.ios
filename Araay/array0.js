// Arreglo de números
const numeros = [10, 20, 30, 40, 50];

// Arreglo de nombres de países
const paises = ["Chile", "Argentina", "Brasil", "Perú", "México"];

// Arreglo de valores booleanos
const booleanos = [true, false, true, false, true];

// Arreglo de emojis
const emojis = ["🌎", "🏖️", "🍂", "🌲", "🌻"]; // usar Windows + .

// Arreglo mixto (combinación de los anteriores)
const mixto = [42, "Japón", false, "🍣", "Programación"];

console.log("Arreglo de Números:", numeros);
console.log("Arreglo de Países:", paises);
console.log("Arreglo de Booleanos:", booleanos);
console.log("Arreglo de Emojis:", emojis);
console.log("Arreglo Mixto:", mixto);

